places = ['Brazil', 'England', 'Australia', 'Disneyland', 'Kitten Land'] # please let Kitten Land be a place.

print(places)

print( sorted(places) )

print(places) # Printing out the original list

print( sorted(places, reverse=True) ) # printing out a reverse alphabetical version of the list

print(places) # Still printing out original list

print( places.reverse() ) # printing out the reverse of the list

print( places.reverse() ) # reversing the list again to show that it's back to its original order

print( places.sort() ) # printing in alphabetical order

print( places.sort(reverse=True) ) # printing in reverse alphabetical order

